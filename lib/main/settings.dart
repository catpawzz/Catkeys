class _SettingsPageState extends State<SettingsPage> {
  final TextEditingController _postsController = TextEditingController();
  String dropdownValue = '100';
  bool hapticFeedback = false;

  @override
  void initState() {
    super.initState();
    fetchSettings();
  }

  @override
  void dispose() {
    _postsController.dispose();
    super.dispose();
