  deleteNote(id) {
    try {
      client.notes.delete(
        NotesDeleteRequest(
          noteId: id,
        ),
      );
      Fluttertoast.showToast(
        msg: 'Note deleted successfully!',
        toastLength: Toast.LENGTH_LONG,
        gravity: ToastGravity.TOP,
        backgroundColor: Theme.of(context).colorScheme.primaryContainer,
        textColor: Theme.of(context).colorScheme.onPrimaryContainer,
      );
    } catch (e) {
      Fluttertoast.showToast(
        msg: 'There was an error while deleting the note!',
        toastLength: Toast.LENGTH_LONG,
        gravity: ToastGravity.TOP,
        backgroundColor: Theme.of(context).colorScheme.onErrorContainer,
        textColor: Theme.of(context).colorScheme.error,
      );
    }
  }

  Future<List<DriveFile>> fetchDriveItems() async {
    if (currentPageIndex != 2) {
      return []; // Return an empty list if currentPageIndex is not 2
    }
    try {
      await Future.delayed(const Duration(milliseconds: 100));
      final response = await client.drive.files.files(
        DriveFilesRequest(
          limit: posts,
        ),
      );
      if (kDebugMode) {
        //print('------------------ DRIVE DEBUG ------------------');
        //print(response);
        //print('------------------ DRIVE DEBUG ------------------');
      }
      double totalSize = response.fold(0, (sum, file) => sum + file.size);
      setState(() {
        userStorageUsed =
            double.parse((totalSize / (1024 * 1024 * 1024)).toStringAsFixed(2));
      });
      return response.toList();
    } catch (e) {
      return []; // Return an empty list on error
    }
  }

