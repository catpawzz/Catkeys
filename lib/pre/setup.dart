class Setup extends StatelessWidget {
  const Setup({super.key});

  // This widget is the root of your application.
  @override
  Widget build(BuildContext context) {
    return DynamicColorBuilder(
      builder: (ColorScheme? lightDynamic, ColorScheme? darkDynamic) {
        return MaterialApp(
          title: 'Setup',
          themeMode: ThemeMode.system, // Use device's color scheme
          theme: ThemeData(
            useMaterial3: true,
            colorScheme: lightDynamic?.harmonized() ??
                ColorScheme.fromSeed(seedColor: Colors.purple),
            fontFamily: 'Inter', // Set the font family to Inter
          ),
          darkTheme: ThemeData(
            useMaterial3: true,
            colorScheme: darkDynamic?.harmonized() ??
                ColorScheme.fromSeed(
                    seedColor: Colors.purple, brightness: Brightness.dark),
            fontFamily: 'Inter',
          ),
          home: const SetupPage(title: 'Setup'),
        );
      },
    );
  }
}

class SetupPage extends StatefulWidget {
  const SetupPage({super.key, required this.title});

  final String title;

  @override
  State<SetupPage> createState() => _SetupPageState();
}

class _SetupPageState extends State<SetupPage> {
  final TextEditingController _urlController = TextEditingController();
  final TextEditingController _tokenController = TextEditingController();

  @override
  void dispose() {
    _urlController.dispose();
    super.dispose();
  }

  navHome() {
    Navigator.push(
      context,
      MaterialPageRoute(builder: (context) => const HomePage(title: 'Catkeys')),
    );
  }

  checkInstance() {
    String url = _urlController.text;
    String token = _tokenController.text;
    showDialog(
      context: context,
      builder: (BuildContext context) {
        return AlertDialog(
          title: const Text('Please confirm'),
          content: Text('URL: $url\n\nTOKEN: $token'),
          actions: [
            TextButton(
              onPressed: () {
                Navigator.of(context).pop();
              },
              child: const Text('Cancel'),
            ),
            TextButton(
              onPressed: () {
                saveData(url, token);
              },
              child: const Text('Confirm'),
            ),
          ],
        );
      },
    );
  }

  saveData(String url, String token) async {
    SharedPreferences prefs = await SharedPreferences.getInstance();
    await prefs.setString('catkeys_url', url);
    await prefs.setString('catkeys_token', token);
    await prefs.setInt('catkeys_posts_shows', 100);
    await prefs.setBool('catkeys_haptics', true);
    navHome();
  }

  vibrateSelection() async {
    final hasCustomVibrationsSupport =
        await Vibration.hasCustomVibrationsSupport();
    if (hasCustomVibrationsSupport != null && hasCustomVibrationsSupport) {
      Vibration.vibrate(duration: 50);
    } else {
      Vibration.vibrate();
      await Future.delayed(const Duration(milliseconds: 50));
      Vibration.vibrate();
    }
  }

  vibrateError() async {
    final hasCustomVibrationsSupport =
        await Vibration.hasCustomVibrationsSupport();
    if (hasCustomVibrationsSupport != null && hasCustomVibrationsSupport) {
      Vibration.vibrate(duration: 200);
    } else {
      Vibration.vibrate();
      await Future.delayed(const Duration(milliseconds: 200));
      Vibration.vibrate();
    }
  }

